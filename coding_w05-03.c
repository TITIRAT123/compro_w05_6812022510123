#include <stdio.h>  // เรียกใช้ไลบรารีมาตรฐานสำหรับฟังก์ชัน printf

int main() {
    // บรรทัดที่ 1: แสดงตัวแปร a และค่าต่างๆ พร้อมจัดรูปแบบตามโจทย์
    // ใช้ %% เพื่อแสดงเครื่องหมาย %
    printf("a = 0 . 6 1 7 %%\n");

    // บรรทัดที่ 2: แสดงตัวแปร b โดยจัดช่องว่างให้เหมือนในโจทย์
    // มีช่องว่างหน้าเลข 2 และระหว่าง 2 กับ 5
    printf("b =   2   5 %%\n");

    // บรรทัดที่ 3: แสดงตัวแปร c และค่า 1, 9, จุดทศนิยม, ศูนย์สามตัว และ %
    printf("c = 1 9 . 0 0 0 %%\n");

    // บรรทัดที่ 4: แสดงตัวแปร d และเลข 9 โดยมีช่องว่างหน้า 9
    printf("d =     9 %%\n");

    // บรรทัดที่ 5: แสดงตัวแปร e กับเลข 1 2 4 และเลข 5 สองตัว พร้อม %
    // มีช่องว่างระหว่างเลข 4 กับ 5 ตัวแรก
    printf("e = 1 2 4   5 5 %%\n");

    // บรรทัดที่ 6: แสดงตัวแปร f กับเลข 0 จุดทศนิยม ศูนย์สองตัว และเลข 1
    printf("f = 0 . 0 0 1 %%\n");

    return 0;  // คืนค่าศูนย์เพื่อบอกว่าโปรแกรมทำงานสมบูรณ์
}
